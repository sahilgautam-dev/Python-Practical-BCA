#Q15. Write a program using built-in functions on list, string, and dictionar

#LIST 
nums = [10, 20, 30, 40, 50]

print("List:", nums)
print("Length of list:", len(nums))
print("Maximum value:", max(nums))
print("Minimum value:", min(nums))
print("Sum of elements:", sum(nums))

# STRING 
text = "python"

print("\nString:", text)
print("Length of string:", len(text))
print("Uppercase:", text.upper())
print("Lowercase:", text.lower())
print("Reversed string:", text[::-1])

# DICTIONARY 
student = {"name": "Sahil", "age": 20, "city": "Delhi"}

print("\nDictionary:", student)
print("Keys:", student.keys())
print("Values:", student.values())
print("Items:", student.items())
print("Length of dictionary:", len(student))